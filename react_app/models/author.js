class Author {
    constructor(
      id,
      name,
      slug,
      description,
      createdAt,
      updatedAt,
      deletedAt
    ) {
      this.id = id;
      this.name = name;
      this.slug = slug;
      this.description = description;
      this.createdAt = createdAt;
      this.updatedAt = updatedAt;
      this.deletedAt = deletedAt;
    }
  }
  
  export default Author;
  