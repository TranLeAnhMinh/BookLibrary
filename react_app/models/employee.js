class Employee {
    constructor(id, employeeCode, email, fullName, createdAt, updatedAt) {
      this.id = id;
      this.employeeCode = employeeCode;
      this.email = email;
      this.fullName = fullName;
      this.createdAt = createdAt;
      this.updatedAt = updatedAt;
    }
  }
  
  export default Employee;
  